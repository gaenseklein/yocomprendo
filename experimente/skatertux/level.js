function change_to_distances(){
  for(let i=0;i<level.length;i++){
    for(let ii=0;ii<level[i].obstacles.length;ii++){
      if(!level[i].obstacles[ii].spawn_distance){
        level[i].obstacles[ii].spawn_distance = level[i].obstacles[ii].spawn_time * game.tux.speed
      }
    }
  }
}
