function create_ramp_up(){
  let ramparr = []
  let rampinc = obstacle_types.ramp_up.height / obstacle_types.ramp_up.width
  console.log(rampinc, obstacle_types.ramp_up.height, obstacle_types.ramp_up.width);
  for(let i=0;i<obstacle_types.ramp_up.width;i++){
    ramparr.push(i*rampinc)
  }
  obstacle_types.ramp_up.ramparr = ramparr
}
function create_paths(){
  let keys = Object.keys(obstacle_types)
  for(let ob=0;ob<keys.length;ob++){
    if(!obstacle_types[keys[ob]].path)continue;
    let path = obstacle_types[keys[ob]].path
    let ypoint = []
    for(let wp=0;wp<path.waypoints.length-1;wp++){
      let startpoint = path.waypoints[wp]
      let endpoint = path.waypoints[wp+1]
      let dify = endpoint.y - startpoint.y
      let len = endpoint.x - startpoint.x
      let difinc = dify/len
      for(let i=0;i<len;i++){
        ypoint[startpoint.x+i]=startpoint.y+(difinc*i)
      }
    }
    path.route=ypoint
  }
}


function create_multisrc(){
  let keys = Object.keys(obstacle_types)
  for(let i=0;i<keys.length;i++){
    if(!obstacle_types[keys[i]].multisrc)continue;
    let ms = obstacle_types[keys[i]].multisrc
    ms.frame_time = ms.animation_time / ms.count
  }
}
